def Addition(no1,no2):
    ans=no1+no2
    return ans

def Substraction(no1,no2):
    ans=no1-no2
    return ans

def Multiplication(no1,no2):
    ans=no1*no2
    return ans

def Division(no1,no2):

    if(no2==0):
        print("Divide by zero is not possible")
        return 0

    ans=no1/no2
    return ans